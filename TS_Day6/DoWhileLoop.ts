//do-while loop: A do-while loop always executes at least once before checking the condition.

/*
syntax:

do {
    //statements;
}while(condition);

*/

//Example1: 1.....5

let i:number=1;
do{
    console.log(i);
    i++;
}while(i<=5);

//Example2: 10.....1
let j:number=10;
do{
    console.log(j);
    j--;
}while(j>=1);

